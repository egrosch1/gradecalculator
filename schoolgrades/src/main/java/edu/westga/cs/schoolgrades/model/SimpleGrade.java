public class SimpleGrade implements Grade{

	private double value;

	/**
	 * Getter for Value
	 * @return the value
	 */
	public double getValue() {
		return value;
	}

	/**
	 * Setter for Value
	 * @param value the value to set
	 */
	public void setValue(double value) {
		this.value = value;
	}
	
	
}
